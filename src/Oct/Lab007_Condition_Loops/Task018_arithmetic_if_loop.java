package Oct.Lab007_Condition_Loops;

public class Task018_arithmetic_if_loop
{
    public static void main(String[] args) {
        boolean a = true;
        a = !a;
        if (2 + 2 < 4) {
            System.out.println("Inside the loop");
        }
        System.out.println("Outside -> " + a);
    }
}
